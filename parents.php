<h2>An Integral Part of the University of Iowa</h2>
<p>Being the official campus store, we are proud to serve our students. Owned and operated by the University of Iowa, the money that students spend with us stays on-campus and goes back into supporting programs improving your student’s experience while at the University.</p>
<p>The Iowa Hawk Shop is dedicated to being your one-stop shop for purchasing course materials, technology, school supplies, Hawkeye gear, and accessories. That is why we are open 7 days a week during the school year, to better support your student’s academic success.</p>
<p>As a part of your student’s path to academic success, we partner with several other programs and departments at the University to support all aspects of the student experience.  Ongoing relationships with Dance Marathon, the Greek Societies, STAT, and many other student groups, allow us to keep a finger on the pulse of campus life, and stay in tune with what your student needs to succeed.</p>

<h2>Textbook Kickoff:</h2>

(Show How to purchase textbooks video)

<p>Hawkeye alumni often think back to their favorite memories of starting out at the University of Iowa. Football games at Kinnick stadium, late night study sessions, and hanging out on the Pentacrest lawn. Unfortunately, learning about textbooks is not one of those memories. As parents of your students, you want to set your students up for success. As an organization, we are not just passionate about the Iowa Hawkeyes, but also about textbook affordability and ensuring that your student has the tools and materials they need for success at our University.</p>
<p>The Iowa Hawk Shop wants to help you tackle your students’ first semester head on by helping them learn about the textbook buying process at our third annual Textbook Kick Off.  We understand purchasing textbooks for the first time can be a confusing experience and we want to help facilitate the process by providing the opportunity for your student to work with our own student employees.  These “textbook experts” are there to ensure all students get the correct course materials in one simple step at the beginning of the semester. By waiting to purchase textbooks until this event, you can help ensure your student gets the correct titles on time for the first day of class.</p>

<h2>Used Textbooks:</h2>

<p>The Iowa Hawk Shop strives to keep textbook prices low in order to save you and your student money. As part of this philosophy, we purchase used books from a variety of sources; wholesale used book companies, online marketplaces, and our own students to give you a less expensive option.</p>
<p>Average retail price for used textbooks is typically at 75% of the current retail price of a new book.</p>

<h2>Textbook Rental:</h2>

<p>By offering a textbook rental program, we can offer significant initial savings. The process is similar to purchasing a textbook, but at a much lower price.  The rental fee allows the student the use of the textbook for the entire semester.  More than likely, renting a textbook will be the cheapest option. If the book is available for rent, it will either be indicated on the shelf in-store, or seen as an available option under a given course on hawkshop.com.   All rental textbooks are due back by the Monday after finals week.</p>


<h2>Digital Textbooks:</h2>

<p>Another cost saving alternative is digital textbooks. The Iowa Hawk Shop offers digital e-books that are the electronic form of a textbook. Each eBook is sold as an access code which is used to gain admittance to a specific academic website. In most cases, the digital content is exactly the same as the physical copy.  Unfortunately, digital textbooks are not eligible for returns, refunds, or exchanges once the access code has been used.</p>

<h2>Price Comparison Tool:</h2>

<p>To ensure that we are doing everything we can to provide textbooks at the lowest prices possible, we also provide an online price comparison tool if you would like to check the most common online marketplace sites—Amazon, Half.com, etc.  Even if we are not your first choice, we want you to shop with confidence knowing that you are getting the right book at the best price.</p>

(Link to Compare and Save)

<h2>How Textbook Buy Back Works:</h2>

<p>Textbook Buy Back is a way we can help reduce the cost of textbooks for our students.  At the beginning and end of each semester, we will purchase many of the textbooks used on campus for up to 50% of the new value.  Prices vary depending on many factors.  Prior to buyback, if the store has received a faculty order for the book to use in the next term, the Iowa Hawk Shop will pay up to 50% of the current book price. If we have not received the faculty order, the value of the book is determined by the used book wholesalers, based on national demand.</p>

(Link to Buyback)

<h2>Supplies</h2>

<p>The Iowa Hawk Shop strives to do our best to supply your student with the school supplies they need to succeed in the classroom.  We carry a wide variety of binders, notebooks, pens, pencils, highlighters, folders, backpacks, etc. to choose from as well as Blue books, and calculators for the classroom.  We also have a selection of art supplies available such as mat boards, painting supplies, drawing papers and portfolios.</p>

(Link to supplies)


<h2>Major Tech:</h2>

<p>In a world of options, we understand that choosing a computer for your student can be confusing. Let our Major Tech program help guide your purchasing decisions.. We believe that your student’s intended major can serve as a helpful guide to finding a technology solution. In preparation for the academic year, we have been working in collaboration with departments across campus to help us educate you on which computer best fits your specific needs. Just talk to one of our tech experts to find out what computer works best for your student, or checkout the Major Tech page on www.hawkshop.com.</p>
<p>Our tech advice + Your brain = Major Success!</p>

(Link to Major Tech)

<h2>Technology Options:</h2>

<p>Working with a budget? We can help with that! We offer a variety of educational pricing available only to students, faculty, and staff that can help you save up to $200 off the retail price depending on the product and manufacturer. We also offer a technology trade-in program that allows for you to trade in your old qualifying device in exchange for Hawk Shop/Tech Connection credit towards your new technology purchase.</p>
<p>As a certified Apple® Campus Store, we offer top-notch options in technology.  If and when you are ready to purchase your computer, just visit the Tech Connection in the Iowa Hawk Shop, or the Tech page on our website and check out our options for store pick up and on-campus delivery.</p>
<p>Here at Tech Connection, our Apple® Service Center is dedicated to your student’s success and makes performing repairs and upgrades easy and convenient with our location right on the ground floor of the student union. Warranty repairs are always free, while out-of-warranty services are competitively priced with our student customer base in mind.</p>

(Link to Tech)

<h2>Hawkeye Gear</h2>

<p>Let the Iowa Hawk Shop be your students place to get all of their Hawkeye Spirit wear and gifts.  The Iowa Hawk Shop carries a large assortment of apparel and gifts to suit any student, parent, alum or fan.  We carry a variety of name brands such as Nike, Under Armour, Champion, JanSport and Retro Brand at price levels to suit all customers.  We also have a great assortment of gift items, and products to spruce up their dorm rooms or apartments and make it their own.</p>

(Link to New Arrivals)


<h2>In-Store Pickup:</h2>

<p>Order your textbooks, supplies or Hawkeye gear online and pick them up in-store with our easy in-store pickup options. Go online to www.hawkshop.com, order your items, and pick them up at our convenient on-campus locations: Iowa Hawk Shop on the Ground Floor of the Iowa Memorial Union or Wild Rose Books at the University of Iowa Hospitals and Clinics.  There are two payment options for in-store pickup. 1.) Pay-at-Pickup--this allows you to order your items and select the payment method at point of pickup, including charging the order to your U-Bill. 2.) Pre-Paid Pickup—this allows the payment to be made by credit card or Iowa Hawk Shop gift card in advance through our website. Students need only present a picture ID at the point of pickup to receive their items.</p>




<h2>Free Dorm Delivery:</h2>

<p>To help make textbook ordering easier, the Iowa Hawk Shop will be offering FREE Dorm Delivery for all orders placed on www.hawkshop.com. All orders received by 11am Monday thru Friday, will be delivered to the specified residence hall within one business day. Once delivered, your student can pick up their package at the designated mail desk for the residence hall.  Most dorms will have the packages available for pickup each day by 6pm.  And this service is not just for textbooks, any item ordered on www.hawkshop.com is eligible for free dorm delivery!</p>


<h2>Payment</h2>

<p>The Iowa Hawk Shop offers several easy ways to pay.  We accept all major credit cards, traveler’s checks, personal checks, and of course cash. If you prefer, you can also pay with your U-Bill (University Account). We offer easy charging to your student’s university account thru their ID card. The ID card is swiped at the register when checking out, and the total amount of the transaction is billed at a later time.  Please contact the IMU Business Office for more information on how the U-Bill works.</p>

